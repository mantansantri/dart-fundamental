void main(){

// != (tidak sama dengan)
int num1 = 10;
int num2 = 9;

if(num1 != num2){
    print('true');
} else {
    print('false');
}

// > (lebih dari)
int num3 = 10;
int num4 = 9;

if(num3 > num4){
    print('true');
} else {
    print('false');
}

// <

// == (sama dengan sama dengan)
int num5 = 10;
int num6 = 9;

if(num5 == num6){
    print('true');
} else {
    print('false');
}

// >= (lebih dari sama dengan)
int num7 = 10;
int num8 = 9;

if(num7 >= num8){
    print('true');
} else {
    print('false');
}

// <= (kurang dari sama dengan)
int num9 = 10;
int num10 = 9;

if(num9 <= num10){
    print('true');
} else {
    print('false');
}

}