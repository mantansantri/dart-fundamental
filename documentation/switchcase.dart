void main(){

String day = 'Monday';

switch (day) {
    case 'Monday' :
        print('Hari Senin');
        break;
    case 'Tuesday' :
        print('Hari Selasa');
        break;
    default:
        print('Hari Lainnya');
}

// Switchcase bisa menampung beberapa case seperti diatas.
// Jika isi dari Stirng bukan seperti yang ada di dalam day,
// maka output akan membaca default.

}